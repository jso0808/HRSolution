package evaluation;

// 평가 관리 DTO
public class EvaluationDTO {
	private int year;
	private String id;
	
	
	
	public int getYear() {
		return year;
	}
	public void setYear(int year) {
		this.year = year;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
}
